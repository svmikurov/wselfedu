"""Contains Hexagonal architecture.  # noqa: off

Гексагональная архитектура (Hexagonal Architecture)

Гексагональная архитектура, также известная как "Порты и адаптеры"
(Ports and Adapters), — это шаблон проектирования программного
обеспечения, который предлагает способ организации кода для обеспечения
гибкости, тестируемости и независимости от внешних зависимостей.
Её предложил Алистер Кокберн (Alistair Cockburn) в 2005 году.

Главная цель — отделить бизнес-логику приложения от внешних интерфейсов
(UI, базы данных, API и т. д.).
Это позволяет легко менять внешние компоненты без переписывания основной логики.

Ключевые компоненты

    Ядро (Core)
        Содержит бизнес-логику приложения.
        Не зависит от внешних систем (баз данных, API, фреймворков).
        Определяет порты (интерфейсы) для взаимодействия с внешним
        миром.

    Порты (Ports)
        Интерфейсы, которые определяют, как внешние системы могут
        взаимодействовать с ядром.

        Бывают двух типов:
            Входные порты (Primary/Driving Ports) — для получения команд
            (например, API, CLI).
            Выходные порты (Secondary/Driven Ports) — для взаимодействия
            с внешними сервисами (например, БД, email-сервис).

    Адаптеры (Adapters)
        Реализуют порты и переводят внешние запросы в формат,
        понятный ядру, и наоборот.

        Примеры адаптеров:
            HTTP-адаптер (REST API, GraphQL).
            Database-адаптер (реализация репозитория для PostgreSQL, MongoDB).
            Тестовый адаптер (мок для unit-тестов).

Преимущества

✅ Гибкость — можно заменить базу данных или API без изменения бизнес-логики.
✅ Тестируемость — ядро можно тестировать изолированно, подменяя адаптеры моками.
✅ Чистая архитектура — бизнес-правила не смешаны с инфраструктурным кодом.
✅ Масштабируемость — легко добавлять новые интерфейсы (например, gRPC поверх REST).
"""
